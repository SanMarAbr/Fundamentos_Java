package SentenciasControl;

public class SentenciaIf {
    public static void main(String[] args) {
        var condicion = true;

        if (condicion == true) {
            System.out.println("Es verdadera");
            System.out.println("Nueva linea");
        } else {
            System.out.println("Condicion Falsa");
        }
    }
}
